package MKT;



		public class ReverseString {

		        public static void main(String[] args) {
		  String output="";
		    String s1="Roma";
		    
		    for (int i=s1.length()-1;i>=0;i--)
		    {
		            output=output+s1.charAt(i);
		            
		    }
		System.out.println(output);
	}

}
